package com.symantec.eloqua.assetMigrator.user;

public class User {
	  private String type; 
	  private String id; 
	  private String createdAt; 
	  private String createdBy; 
	  private String depth; 
	  private String description; 
	  private String name; 
	  private String updatedAt; 
	  private String updatedBy; 

	  public String getType(){
	  	return type; 
	  }
	  public void setType(String input){
	  	 this.type = input;
	  }
	  public String getId(){
	  	return id; 
	  }
	  public void setId(String input){
	  	 this.id = input;
	  }
	  public String getCreatedAt(){
	  	return createdAt; 
	  }
	  public void setCreatedAt(String input){
	  	 this.createdAt = input;
	  }
	  public String getCreatedBy(){
	  	return createdBy; 
	  }
	  public void setCreatedBy(String input){
	  	 this.createdBy = input;
	  }
	  public String getDepth(){
	  	return depth; 
	  }
	  public void setDepth(String input){
	  	 this.depth = input;
	  }
	  public String getDescription(){
	  	return description; 
	  }
	  public void setDescription(String input){
	  	 this.description = input;
	  }
	  public String getName(){
	  	return name; 
	  }
	  public void setName(String input){
	  	 this.name = input;
	  }
	  public String getUpdatedAt(){
	  	return updatedAt; 
	  }
	  public void setUpdatedAt(String input){
	  	 this.updatedAt = input;
	  }
	  public String getUpdatedBy(){
	  	return updatedBy; 
	  }
	  public void setUpdatedBy(String input){
	  	 this.updatedBy = input;
	  }
}
  

